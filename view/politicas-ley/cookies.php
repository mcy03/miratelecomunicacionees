            <p class="title">
                <strong>QUÉ SON LAS COOKIES</strong>
            </p>
            <p class="text">
                Las Cookies son archivos que el sitio web o la aplicación que utiliza un usuario instala en su navegador o 
                en su dispositivo durante el recorrido por las páginas o por la aplicación, 
                y sirven para almacenar información sobre la visita. Como la mayoría de los sitios en internet, los portales web deben:
            </p>

            <ul class="lista">
                <li>Asegurar que las páginas web pueden funcionar correctamente.</li>
                <li>Almacenar las preferencias, como el idioma seleccionado o el tamaño de letra.</li>
                <li>Conocer la experiencia de navegación.</li>
                <li>Recopilar información estadística anónima, como las páginas que se han visitado o el tiempo que ha durado la conexión.</li>
            </ul>

            <p class="text">
                El uso de Cookies nos permite optimizar la navegación, adaptando la información y los servicios ofrecidos a los intereses del usuario, para proporcionar una mejor experiencia siempre que visite la página.
            </p>
            <p class="text">
                Las Cookies se asocian únicamente a un usuario anónimo y su ordenador/dispositivo y no proporcionan referencias que permitan conocer datos personales. En todo momento, el usuario podrá acceder a la configuración de su navegador para modificar y/o bloquear la instalación de las cookies enviadas por el sitio web  www.miratelecomunicacions.com. Los usuarios que completen el proceso de registro o hayan iniciado sesión con sus datos de acceso podrán acceder a servicios personalizados y adaptados a sus preferencias según la información personal suministrada en el momento del registro y la almacenada en la Cookie de su navegador.
            </p>
            <p class="text">
                <strong>Google Analytics</strong> almacena cookies para poder elaborar estadísticas sobre el tráfico y volumen de visitas de esta web. Al utilizar este sitio web está consintiendo el tratamiento de información acerca de usted por Google. Por tanto, el ejercicio de cualquier derecho en este sentido deberá hacerlo comunicando directamente con Google. Las cookies de Google Analytics, se almacenan en servidores ubicados en Estados Unidos y se compromete a no compartirla con terceros, excepto en los casos en los que sea necesario para el funcionamiento del sistema o cuando la ley obligue a tal efecto. Según Google, no guarda su dirección IP.
